import java.util.Scanner;

public class exam02 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int x = sc.nextInt();
		
		System.out.println(TopDown(x));

	}
	
	public static int TopDown(int x) {
		int count = 0;
		
		if (x == 1) {
			return count;
		}
		
		
		
		return count;
	}

}
